clear all
f = 2; %发射信号频率
v = 1; %移动台速度，静止情况为零
c = 10;
r0 = 3; %移动台距离基站初始距离
d = 15; %基站距离反射墙的距离
t1 = 0.1:0.0001:12;
E1 = cos(2*pi*f*((1-v/c).*t1-r0/c))./(r0+v.*t1); %直射径信号
E2 = cos(2*pi*f*((1+v/c)*t1+(r0-2*d)/c))./(2*d-r0-v*t1); %反射径信号
figure
plot(t1,E1,t1,E2,"-g",t1,E1-E2,"-r");
legend("直射径信号", "反射经信号", "移动台接收的合成信号");
axis([0 12 -0.5 0.5])
